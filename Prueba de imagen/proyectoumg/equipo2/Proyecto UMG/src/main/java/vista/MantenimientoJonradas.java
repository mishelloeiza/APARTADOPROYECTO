    public void llenadoDeCombos() {
        JornadaDAO jornadaDAO = new JornadaDAO();
        List<Jornada> jornadas = jornadaDAO.select();
        cbox_jornada.addItem("Seleccione una opci√≥n");
        for (int i = 0; i < jornadas.size(); i++) {
            cbox_jornada.addItem(jornadas.get(i).getNombre_jornada());
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("codigo");
        //modelo.addColumn("codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estatus");
        JornadaDAO jornadaDAO = new JornadaDAO();
        List<Jornada> jornadas = jornadaDAO.select();
        tablaJornadas.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < jornadas.size(); i++) {
            dato[0] = Integer.toString(jornadas.get(i).getCodigo_jornada());
            dato[1] = jornadas.get(i).getNombre_jornada();
            dato[2] = jornadas.get(i).getEstatus_jornada();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarJornada() {
        Jornada jornadaAConsultar = new Jornada();
        JornadaDAO jornadaDAO = new JornadaDAO();
        jornadaAConsultar.setCodigo_jornada(Integer.parseInt(txtbuscado.getText()));
        jornadaAConsultar = jornadaDAO.query(jornadaAConsultar);
        txtNombre.setText(jornadaAConsultar.getNombre_jornada());
        txtDireccion.setText(jornadaAConsultar.getEstatus_jornada());
    }

