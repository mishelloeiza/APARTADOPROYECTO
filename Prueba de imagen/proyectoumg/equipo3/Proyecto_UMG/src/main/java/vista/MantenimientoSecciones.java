    public void llenadoDeCombos() {
        SeccionesDAO seccionesDAO = new SeccionesDAO();
        List<Secciones> secciones = seccionesDAO.select();
        cbox_empleado.addItem("Seleccione una opci√≥n");
        for (int i = 0; i < secciones.size(); i++) {
            cbox_empleado.addItem(secciones.get(i).getNombre_seccion());
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Codigo Seccion");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estatus");
        SeccionesDAO seccionesDAO = new SeccionesDAO();
        List<Secciones> secciones = seccionesDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[3];
        for (int i = 0; i < secciones.size(); i++) {
            dato[0] = Integer.toString(secciones.get(i).getCodigo_seccion());
            dato[1] = secciones.get(i).getNombre_seccion();
            dato[2] = secciones.get(i).getEstatus_seccion();
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarVendedor() {
        Secciones seccionesAConsultar = new Secciones();
        SeccionesDAO seccionesDAO = new SeccionesDAO();
        seccionesAConsultar.setCodigo_seccion(Integer.parseInt(txtbuscado.getText()));
        seccionesAConsultar= seccionesDAO.query(seccionesAConsultar);
        txtCodigo.setText(seccionesAConsultar.getNombre_seccion());
        txtNombre.setText(seccionesAConsultar.getEstatus_seccion());
    }

