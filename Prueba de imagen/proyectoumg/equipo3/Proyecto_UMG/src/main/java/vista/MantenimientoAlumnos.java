    public void llenadoDeCombos() {
        alumnoDAO alumnosDAO = new alumnoDAO();
        List<alumno> alumnos = alumnosDAO.select();
        cbox_empleado.addItem("Seleccione una opción");
        for (int i = 0; i < alumnos.size(); i++) {
            cbox_empleado.addItem(alumnos.get(i).getnombrealumno());
        }
    }

    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Carnet Alumno");
        modelo.addColumn("Nombre");
        modelo.addColumn("Dirección");
        modelo.addColumn("Telefono");
        modelo.addColumn("Email");
        modelo.addColumn("Estatus");
        
        alumnoDAO alumnosDAO = new alumnoDAO();
        List<alumno> estudiantes = alumnosDAO.select();
        tablaAlumnos.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < estudiantes.size(); i++) {
            dato[0] = Integer.toString(estudiantes.get(i).getcarnet_alumno());
            dato[1] = estudiantes.get(i).getnombrealumno();
            dato[2] = estudiantes.get(i).getdirealumno();
            dato[3] = estudiantes.get(i).gettelefono();
            dato[4] = estudiantes.get(i).getemailalumno();
            dato[5] = estudiantes.get(i).getEstatus_alumno();

            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

    public void buscarAlumno() {
        alumno alumnosAConsultar = new alumno();
        alumnoDAO alumnosDAO = new alumnoDAO();
        alumnosAConsultar.setcarnet_alumno(Integer.parseInt(txtbuscado.getText()));
        alumnosAConsultar = alumnosDAO.query(alumnosAConsultar);
        txtNombre.setText(alumnosAConsultar.getnombrealumno());
        txtdireccion.setText(alumnosAConsultar.getdirealumno());
        txtTelefono.setText(alumnosAConsultar.gettelefono());
        txtEmail.setText(alumnosAConsultar.getemailalumno());
        txtEstatus.setText(alumnosAConsultar.getEstatus_alumno());
    
    }

