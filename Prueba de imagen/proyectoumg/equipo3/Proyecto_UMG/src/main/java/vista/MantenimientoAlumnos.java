    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        // Crear instancias necesarias
    alumnoDAO alumnosDAO = new alumnoDAO();
    alumno alumnosAEliminar = new alumno();
    
    // Asignar el valor del campo de texto al objeto alumno
    alumnosAEliminar.setcarnet_alumno(Integer.parseInt(txtbuscado.getText()));
    
    // Llamar al método para eliminar
    alumnosDAO.delete(alumnosAEliminar);
    
    // Actualizar la tabla después de eliminar
    llenadoDeTablas(); 

    }//GEN-LAST:event_btnEliminarActionPerformed

