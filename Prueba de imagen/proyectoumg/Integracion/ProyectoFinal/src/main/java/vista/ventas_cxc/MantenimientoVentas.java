    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id cliente");
        modelo.addColumn("No.Venta");
        modelo.addColumn("Id Producto");
        modelo.addColumn("Cantidad");
        modelo.addColumn("Precio  Unitario");
          modelo.addColumn("Total");
        ventasDAO aulaDAO = new ventasDAO();
        List<ventas> aulas = aulaDAO.select();
        tablaAula.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < aulas.size(); i++) {
            dato[0] = Integer.toString(aulas.get(i).getId_cliente());
            dato[1] = aulas.get(i).getNo_venta();
            dato[2] = Integer.toString(aulas.get(i).getId_producto());
          
            dato[3] = Integer.toString(aulas.get(i).getCantidad());
            dato[4] = Double.toString(aulas.get(i).getPrecio_unitario());
              dato[5] = Double.toString(aulas.get(i).getTotal());
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

